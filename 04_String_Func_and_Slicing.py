#STRING FUNCTION

str = "have a nice day"
print(str.capitalize())  #fist letter capital
print(str.count("a"))    #count number in string or return an int valu
print(str.endswith("day"))#return a boolean value
print(str.lower())    #to convert a string in lowercase
print(str.upper())      #to convert a string in uppercase
print(str.find("a"))     #find a value on index
print(str.find("have"))  #find a value on index
print(str.replace("a","is"))#replacing a value in string:

#STING SLICING
print(str[:])   #print the whole string from [0] t0 [n-1]
print(str[0:2])  #print value from [0] to [1][n-1]
print(str[::-1])  #print a reverse string
